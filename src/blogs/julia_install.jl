## download Julia Language binaries

[click here to download Julia Lang](https://julialang.org/downloads/)


Extract zip file any any directory of your choice and change Windows/Linux environment varianbles/system Path to find Julia.exe

*for example*

+ Windows
	$env:PATH
	C:\amit\julia-1.5.0\bin

+ Linux
	export PATH=$PATH:/place/with/the/julia-1.5.0\bin
